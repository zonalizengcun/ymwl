package world.entity;

public class PveSectionEntity {
	private int sectionId;
	
	private int easyStar;
	
	private int hardStar;

	public int getSectionId() {
		return sectionId;
	}

	public void setSectionId(int sectionId) {
		this.sectionId = sectionId;
	}

	public int getEasyStar() {
		return easyStar;
	}

	public void setEasyStar(int easyStar) {
		this.easyStar = easyStar;
	}

	public int getHardStar() {
		return hardStar;
	}

	public void setHardStar(int hardStar) {
		this.hardStar = hardStar;
	}
}
